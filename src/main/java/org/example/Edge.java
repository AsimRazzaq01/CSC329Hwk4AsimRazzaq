package org.example;

/**
 * Edge class
 * variables: int -> v1, v2, weight
 */
public class Edge {
    // Member Variables
    int v1 ;
    int v2 ;
    int weight ;

    // Constructor to set variables
    public Edge(int v1, int v2, int weight) {
        this.v1 = v1;
        this.v2 = v2;
        this.weight = weight;
    }
}
